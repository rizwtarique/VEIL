create table if not exists public.incidents (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  website text not null,
  severity text not null check (severity in ('critical', 'high', 'medium', 'low')),
  risk_score integer not null check (risk_score between 0 and 100),
  findings jsonb,
  prompt_preview text
);

alter table public.incidents enable row level security;

create policy "Allow public incident reads"
on public.incidents
for select
to anon
using (true);

create policy "Allow extension incident inserts"
on public.incidents
for insert
to anon
with check (
  website <> ''
  and severity in ('critical', 'medium', 'low')
  and risk_score between 0 and 100
  and char_length(coalesce(prompt_preview, '')) <= 240
);

create index if not exists incidents_created_at_idx
on public.incidents (created_at desc);

create index if not exists incidents_severity_idx
on public.incidents (severity);

alter publication supabase_realtime add table public.incidents;

